public class Acao {
    private Integer id;
    private String descricao;
    
    public Acao(Integer id, String descricao) {
        this.id = id;
        this.descricao = descricao;
    }

    public Integer getId() {
        return id;
    }

    public String getDescricao() {
        return descricao;
    }    
}
